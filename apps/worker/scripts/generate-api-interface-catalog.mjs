import { existsSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { apiInterfaceGroups } from "../src/modules/api-interface-catalog.mjs";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "../../..");
const modulesDir = resolve(repoRoot, "apps/worker/src/modules");
const outputPath = resolve(repoRoot, "apps/web/src/features/settings/api-interface-catalog.generated.ts");
const methodOrder = ["GET", "POST", "PUT", "PATCH", "DELETE"];
const bodyMethods = new Set(["POST", "PUT", "PATCH"]);
const isCheckMode = process.argv.includes("--check");

function getEndpointKey(endpoint) {
  return `${endpoint.method} ${endpoint.path}`;
}

function listWorkerRoutes() {
  return readdirSync(modulesDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .flatMap((entry) => {
      const routesPath = resolve(modulesDir, entry.name, "routes.ts");
      if (!existsSync(routesPath)) return [];

      const source = readFileSync(routesPath, "utf8");
      return Array.from(source.matchAll(/app\.(get|post|put|patch|delete)\("([^"]+)"/g), ([, method, path]) => `${method.toUpperCase()} ${path}`);
    })
    .sort();
}

function listCatalogRoutes() {
  return apiInterfaceGroups.flatMap((group) => group.endpoints.map(getEndpointKey)).sort();
}

function formatList(items) {
  return items.length === 0 ? "  (none)" : items.map((item) => `  - ${item}`).join("\n");
}

function validateCatalog() {
  const errors = [];
  const seen = new Set();

  for (const group of apiInterfaceGroups) {
    for (const field of ["id", "kicker", "title", "description"]) {
      if (!group[field]) errors.push(`Group ${group.id ?? "(missing id)"} is missing ${field}.`);
    }

    if (!Array.isArray(group.endpoints) || group.endpoints.length === 0) {
      errors.push(`Group ${group.id ?? "(missing id)"} must include at least one endpoint.`);
      continue;
    }

    for (const endpoint of group.endpoints) {
      const key = getEndpointKey(endpoint);
      if (seen.has(key)) errors.push(`Duplicate endpoint in catalog: ${key}`);
      seen.add(key);

      if (!methodOrder.includes(endpoint.method)) errors.push(`Invalid method for ${key}.`);
      if (!endpoint.path?.startsWith("/api/")) errors.push(`Endpoint path must start with /api/: ${key}`);

      for (const field of ["title", "description", "access"]) {
        if (!endpoint[field]) errors.push(`Endpoint ${key} is missing ${field}.`);
      }

      if (bodyMethods.has(endpoint.method) && !Object.hasOwn(endpoint.example ?? {}, "requestBody")) {
        errors.push(`Endpoint ${key} must define example.requestBody, using null when the route has no JSON body.`);
      }
    }
  }

  const workerRoutes = listWorkerRoutes();
  const catalogRoutes = listCatalogRoutes();
  const workerRouteSet = new Set(workerRoutes);
  const catalogRouteSet = new Set(catalogRoutes);
  const missingFromCatalog = workerRoutes.filter((route) => !catalogRouteSet.has(route));
  const extraInCatalog = catalogRoutes.filter((route) => !workerRouteSet.has(route));

  if (missingFromCatalog.length > 0) {
    errors.push(`Routes missing from API interface catalog:\n${formatList(missingFromCatalog)}`);
  }

  if (extraInCatalog.length > 0) {
    errors.push(`Catalog entries without matching worker routes:\n${formatList(extraInCatalog)}`);
  }

  if (errors.length > 0) {
    throw new Error(errors.join("\n\n"));
  }
}

function buildGeneratedSource() {
  return `// Generated by apps/worker/scripts/generate-api-interface-catalog.mjs. Do not edit manually.

export type ApiMethod = "GET" | "POST" | "PUT" | "PATCH" | "DELETE";

export type ApiEndpointExample = {
  headers?: string[];
  pathParameters?: Record<string, string>;
  queryParameters?: Record<string, string>;
  requestBody?: unknown | null;
};

export type ApiEndpoint = {
  access: string;
  description: string;
  example?: ApiEndpointExample;
  method: ApiMethod;
  path: string;
  title: string;
};

export type ApiInterfaceGroup = {
  description: string;
  endpoints: ApiEndpoint[];
  id: string;
  kicker: string;
  title: string;
};

export const apiInterfaceGroups: ApiInterfaceGroup[] = ${JSON.stringify(apiInterfaceGroups, null, 2)};
`;
}

try {
  validateCatalog();
  const generatedSource = buildGeneratedSource();
  const currentSource = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : null;

  if (isCheckMode) {
    if (currentSource !== generatedSource) {
      throw new Error("Generated API interface catalog is stale. Run `pnpm api-catalog:generate`.");
    }
    process.exit(0);
  }

  if (currentSource !== generatedSource) {
    writeFileSync(outputPath, generatedSource, "utf8");
  }
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}
